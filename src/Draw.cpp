#include "BmpCore.h"


//******************************************************************************
// macro
//******************************************************************************

#define DIR "../bmp/draw/"


//******************************************************************************
// declaration of function
//******************************************************************************

static void BmpDrawX();


//******************************************************************************
// definition of function
//******************************************************************************

//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
int main(int argc, char *argv[]) {
	BmpDrawX();
	return 0;
}


//------------------------------------------------------------------------------
//
//------------------------------------------------------------------------------
static void BmpDrawX() {
	CBmp bmp;
	bmp.Init(60, 60);
	TPoint startPoint = {0, 0};
	TPoint endPoint = {59, 59};
	TRGB rgb;
	rgb.blue = 0xff;
	rgb.green = 0;
	rgb.red = 0;
	BmpDrawLine(bmp, startPoint, endPoint, rgb);

	startPoint = {0, 59};
	endPoint = {59, 0};
	rgb.blue = 0;
	rgb.green = 0;
	rgb.red = 0xff;
	BmpDrawLine(bmp, startPoint, endPoint, rgb);
	bmp.Save(DIR "draw_X.bmp");	
}

